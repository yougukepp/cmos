/*---------------------------------- 预处理区 ---------------------------------*/

/************************************ 头文件 ***********************************/
#include "typedef.h"
#include "stm32f4xx_hal.h"
#include "switch.h"

/*----------------------------------- 声明区 ----------------------------------*/

/********************************** 变量声明区 *********************************/
/********************************************************************************
 *
 * 变量功能: 系统的时钟值 每次进入SysTick中断自己加一次
 * 取值范围: 0 - 0xFFFFFFFF
 * 注意事项: 无
 *
 *******************************************************************************/
static cm_uint32_t s_tick = 0;

/********************************** 函数声明区 *********************************/

/********************************** 变量实现区 *********************************/

/********************************** 函数实现区 *********************************/

/*******************************************************************************
 *
 * 函数名  : SysTick_Handler
 * 负责人  : 彭鹏
 * 创建日期：20150321 
 * 函数功能: SysTick ISR
 *
 * 输入参数: 无
 *
 * 输出参数: 无
 *
 * 返回值  : 无
 *          
 * 调用关系: 无
 * 其 它   : 无
 *
 ******************************************************************************/
void SysTick_Handler(void)
{
    s_tick++;

    /* 处理time_slice */
    switch_update_timeslice();

    /* 悬起PendSV异常(此时必然为咬尾中断) 准备任务切换 */
    SCB->ICSR |= SCB_ICSR_PENDSVSET_Msk;
}

