/*******************************************************************************
 *
 * 函数名  : SVC_Handler_C
 * 负责人  : 彭鹏
 * 创建日期：20150409 
 * 函数功能: 系统调用主逻辑
 *
 * 输入参数: sp任务堆栈
 *
 * 输出参数: 无
 *
 * 返回值  : 调用状态
 *          
 * 调用关系: 无
 * 其 它   : 无
 *
 ******************************************************************************/
int32 SVC_Handler_C(const uint32 *sp)
{
    uint8 svc_number = 0;
    svc_number = ((uint8 *) sp[6])[-2];

    s_syscall_table[svc_number]();

    return 0;
}



