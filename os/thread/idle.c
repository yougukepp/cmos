static void cm_idle_thread(void const *argument)
{
    cm_int32_t i = 0;
    while (1)
    {
        while(i < 0x1fffff)
        {
            i++;
        }
        BSP_LED_Toggle(LED3);
        i = 0;
    }
}

void idle_create(void)
{
    osThreadCreate(osThread(cm_idle_thread), NULL);
}



