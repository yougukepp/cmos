/*******************************************************************************
*
* 函数名  : cmos_strcat
* 负责人  : 彭鹏
* 创建日期: 20151221
* 函数功能: 实现strcat功能 末尾添加NUL
*
* 输入参数: src 源地址
* 输出参数: dst 目的地址
* 返回值  : 无
* 调用关系: 无
* 其 它   : 无
*
******************************************************************************/
void cmos_strcat(cmos_uint8_T *dst, const cmos_uint8_T *src, cmos_int32_T len)
{
    strcat((char*)dst, (char*)src);
    dst[len] = NULL;
}

