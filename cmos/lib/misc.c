/*******************************************************************************
*
* 函数名  : cmos_assert
* 负责人  : 彭鹏
* 创建日期: 20151218
* 函数功能: 断言
*
* 输入参数: flag 断言bool变量
*                TRUE  啥也不干
*                FLASE 出错打印+死循环
*           file 出错源文件名字
*           line 出错源文件行数
*
* 输出参数: 无
* 返回值  : 无
* 调用关系: 无
* 其 它   : 无
*
******************************************************************************/
#if (CMOS_DEBUG_LEVEL > 0)
inline void cmos_assert(cmos_bool_T flag, uint8_t* file, uint32_t line)
{
    if(!flag)
    {
        cmos_err_log("%s,%d: assert failed!", file, line);
        while(1);
    }
}
#else
inline void cmos_assert(cmos_bool_T flag, uint8_t* file, uint32_t line){}
#endif

