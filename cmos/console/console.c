/************************************ 头文件 ***********************************/
#include <stdarg.h>
#include <stdio.h>
#include "cmos_config.h"
#include "uart.h"
#include "console.h"

/*----------------------------------- 声明区 ----------------------------------*/

/********************************** 变量声明区 *********************************/
static char s_printf_buf[CMOS_PRINTF_BUF_SIZE] = {0};

/********************************** 函数声明区 *********************************/

/********************************** 函数实现区 *********************************/
/*******************************************************************************
*
* 函数名  : console_init
* 负责人  : 彭鹏
* 创建日期: 20150614
* 函数功能: 控制台初始化
*
* 输入参数: 无
*
* 输出参数: 无
*
* 返回值  : 无
*
* 调用关系: 无
* 其 它   : 无
*
******************************************************************************/
cmos_int32_T console_init(cmos_int32_T baud_rate)
{
    uart_init(CMOS_CONSOLE_UART, baud_rate);
    return cmos_OK_E;
}

/*******************************************************************************
*
* 函数名  : printf_poll
* 负责人  : 彭鹏
* 创建日期: 20150614
* 函数功能: uart轮询方式输出(与中断、DMA模式共存)
*
* 输入参数: 无
*
* 输出参数: 无
*
* 返回值  : 无
*
* 调用关系: 无
* 其 它   : 系统错误或调试打印需要第一时间输出 采用轮询uart方式
*
******************************************************************************/
cmos_int32_T printf_poll(char *fmt, ...)
{
    va_list args;
    int n = 0;
   
    va_start(args, fmt);
    n = vsprintf(s_printf_buf, fmt, args);
    va_end(args);
	
    //uart_send(s_printf_buf, n);

    return n;
}

