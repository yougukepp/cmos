/*******************************************************************************
*
* 函数名  : hal_init
* 负责人  : 彭鹏
* 创建日期: 20150615
* 函数功能: cmos hal初始化
*
* 输入参数: 无
*
* 输出参数: 无
*
* 返回值  : 函数执行状态
*
* 调用关系: 无
* 其 它   : 无
*
******************************************************************************/
cmos_status_T hal_init(void)
{
    /* stm32 hal 初始化 */
    if(HAL_OK != HAL_Init())
    {
        assert_failed(__FILE__, __LINE__);
    }

    return cmos_OK_E;
}

