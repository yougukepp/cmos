static void job1 (void const *argv);
osThreadDef(job1, osPriorityNormal, 1, 0x1000);

/********************************** 变量实现区 *********************************/


/********************************** 函数实现区 *********************************/
/*******************************************************************************
*
* 函数名  : main
* 负责人  : 彭鹏
* 创建日期: 20150321
* 函数功能: 操作系统测试桩主函数
*
* 输入参数: 无
*
* 输出参数: 无
*
* 返回值  : 无
*
* 调用关系: startup_<device_name>.s 调用的第二个函数
* 其 它   : 无
*
******************************************************************************/
int main(void)
{
    osKernelInitialize();
    osThreadCreate(osThread(job1), NULL);
    osKernelStart();

    while(1);
}

static void job1 (void const *argument)
{
    while (1)
    {
        osDelay(500); /* 延迟500ms */
        BSP_LED_Toggle(LED4);
    }
}
